class TileExpertSystem:
    def __init__(self):
        self.facts = {}

    def ask(self, question: str, options: list) -> str:
        print(f"\n{question}")
        for i, opt in enumerate(options, 1):
            print(f"  {i}. {opt}")
        while True:
            try:
                choice = int(input("   Ваш выбор (номер): "))
                if 1 <= choice <= len(options):
                    return options[choice - 1]
                else:
                    print("   Ошибка: введите номер из списка!")
            except ValueError:
                print("   Ошибка: введите число!")

    def collect_input(self):
        print("="*70)
        print("   ЭКСПЕРТНАЯ СИСТЕМА: Выбор способа укладки плитки")
        print("="*70)

        self.facts['surface'] = self.ask("1. Тип поверхности", ["Пол", "Стена", "Потолок"])
        self.facts['base_material'] = self.ask("2. Материал основания", ["Бетон", "Гипсокартон", "Дерево/ДСП", "Штукатурка", "Металл", "Другое"])
        self.facts['base_condition'] = self.ask("3. Состояние основания", ["Идеально ровное", "С небольшими неровностями", "С трещинами/дефектами", "Требует капитального выравнивания"])
        self.facts['area'] = self.ask("4. Площадь укладки", ["До 5 м²", "5–15 м²", "15–30 м²", "Более 30 м²"])
        self.facts['tile_type'] = self.ask("5. Тип плитки", ["Керамика", "Керамогранит", "Мозаика", "Клинкер", "Другое"])
        self.facts['tile_size'] = self.ask("6. Размер плитки", ["Маленькая (до 20×20 см)", "Средняя (20–40 см)", "Большая (свыше 40 см)"])
        self.facts['tile_shape'] = self.ask("7. Форма плитки", ["Квадрат", "Прямоугольник", "Шестигранник", "Другая"])
        self.facts['room_type'] = self.ask("8. Тип помещения", ["Ванная/санузел", "Кухня", "Гостиная/коридор", "Балкон/лоджия", "Наружные работы", "Другое"])
        self.facts['humidity'] = self.ask("9. Уровень влажности", ["Сухое помещение", "Влажное", "Мокрое (постоянный контакт с водой)"])
        self.facts['warm_floor'] = self.ask("10. Наличие теплого пола", ["Да", "Нет"])
        self.facts['load'] = self.ask("11. Ожидаемая нагрузка", ["Низкая (пешеходная)", "Средняя", "Высокая"])
        self.facts['budget'] = self.ask("12. Бюджет", ["Низкий", "Средний", "Высокий"])
        self.facts['experience'] = self.ask("13. Уровень опыта укладчика", ["Новичок", "Средний", "Профессионал"])
        self.facts['tools'] = self.ask("14. Наличие профессиональных инструментов (вкл. СВП)", ["Да", "Нет"])
        self.facts['time_limit'] = self.ask("15. Ограничение по времени", ["Строго ограничено", "Достаточно времени"])
        self.facts['hydro'] = self.ask("16. Требуется гидроизоляция", ["Да", "Нет"])
        self.facts['svp_want'] = self.ask("17. Хотите использовать СВП", ["Да", "Нет"])
        self.facts['glue_pref'] = self.ask("18. Предпочтение типа клея", ["Стандартный", "Эластичный", "Для влажных помещений", "Быстросохнущий", "Другое"])
        self.facts['grout_pref'] = self.ask("19. Требования к затирке", ["Стандартная цементная", "Эпоксидная", "Антигрибковая", "Цветная/декоративная"])
        self.facts['pattern'] = self.ask("20. Желаемый рисунок укладки", ["Прямая", "Диагональная", "Елочкой", "Со смещением", "Модульная", "Другое"])
        self.facts['temp_mode'] = self.ask("21. Температурный режим", ["Стандартный (18–25°C)", "Низкие температуры", "Высокие температуры"])
        self.facts['frost'] = self.ask("22. Требуется морозостойкость", ["Да", "Нет"])
        self.facts['outdoor'] = self.ask("23. Внутренние или наружные работы", ["Внутренние", "Наружные"])
        self.facts['anti_slip'] = self.ask("24. Требуется антискольжение", ["Да", "Нет"])
        self.facts['eco'] = self.ask("25. Экологичность материалов важна", ["Да", "Нет"])
        self.facts['allergy'] = self.ask("26. Есть аллергия на химические вещества", ["Да", "Нет"])
        self.facts['leveling'] = self.ask("27. Необходимость предварительного выравнивания", ["Да", "Нет"])
        self.facts['joint_width'] = self.ask("28. Предпочтительная ширина швов", ["Тонкие (1–2 мм)", "Средние (3–5 мм)", "Широкие (более 5 мм)"])
        self.facts['price_level'] = self.ask("29. Цена за м²", ["Низкая", "Средняя", "Высокая"])
        self.facts['availability'] = self.ask("30. Доступность материалов", ["В наличии", "Ограничена"])
        self.facts['style'] = self.ask("31. Стиль дизайна", ["Современный", "Классический", "Минимализм", "Другое"])
        self.facts['communications'] = self.ask("32. Наличие коммуникаций под плиткой", ["Да", "Нет"])
        self.facts['tile_thickness'] = self.ask("33. Толщина плитки", ["Тонкая (до 8 мм)", "Стандартная", "Толстая (свыше 12 мм)"])
        self.facts['soundproof'] = self.ask("34. Требуется шумоизоляция", ["Да", "Нет"])
        self.facts['adhesion'] = self.ask("35. Тип основания по адгезии", ["Высокая", "Средняя", "Низкая (требует грунтовки)"])

        print("\n" + "="*70)
        print("Анализ завершён. Формируем рекомендации...\n")

    def infer(self):
        recs = []

        # ==================== ОСНОВНЫЕ ПРАВИЛА ====================
        if self.facts['surface'] == "Пол" and self.facts['base_condition'] == "Идеально ровное":
            recs.append("Способ укладки: ПРЯМАЯ укладка")

        if self.facts['tile_size'] == "Большая (свыше 40 см)":
            recs.append("Обязательно используйте СВП")

        if self.facts['humidity'] == "Мокрое (постоянный контакт с водой)" or self.facts['room_type'] == "Ванная/санузел":
            recs.append("ГИДРОИЗОЛЯЦИЯ основания ОБЯЗАТЕЛЬНА")
            recs.append("Клей: Для влажных помещений")

        if self.facts['warm_floor'] == "Да":
            recs.append("Клей: Эластичный")

        if self.facts['base_condition'] in ["С трещинами/дефектами", "Требует капитального выравнивания"] or self.facts['leveling'] == "Да":
            recs.append("Сначала выполните выравнивание основания")

        if self.facts['pattern'] in ["Диагональная", "Елочкой", "Модульная"]:
            if self.facts['experience'] != "Профессионал":
                recs.append("ВНИМАНИЕ: Для выбранного рисунка рекомендуется ПРОФЕССИОНАЛЬНЫЙ уровень")

        if self.facts['load'] == "Высокая":
            recs.append("Рекомендуется КЕРАМОГРАНИТ + усиленный клей")

        if self.facts['outdoor'] == "Наружные" or self.facts['frost'] == "Да":
            recs.append("Клей: Морозостойкий")
            recs.append("Затирка: Эпоксидная")

        if self.facts['experience'] == "Новичок":
            recs.append("Новичкам рекомендуется: Прямая или со смещением + СВП")

        if self.facts['budget'] == "Низкий" or self.facts['price_level'] == "Низкая":
            recs.append("Бюджетный вариант: Прямая укладка + стандартная цементная затирка")

        if self.facts['allergy'] == "Да" or self.facts['eco'] == "Да":
            recs.append("Использовать только низкотоксичные / экологичные материалы")

        if self.facts['anti_slip'] == "Да" and self.facts['surface'] == "Пол":
            recs.append("Плитка с антискользящим покрытием")

        if self.facts['tile_type'] == "Мозаика":
            recs.append("Укладка мозаики на специальную сетку + белый клей")

        if self.facts['communications'] == "Да":
            recs.append("Требуется аккуратная резка плитки вокруг коммуникаций")

        if self.facts['adhesion'] == "Низкая (требует грунтовки)":
            recs.append("Обязательно нанесите грунтовку")

        if self.facts['soundproof'] == "Да":
            recs.append("Использовать шумоизоляционную подложку")

        if self.facts['temp_mode'] == "Низкие температуры":
            recs.append("Клей: Быстросохнущий или с добавками для низких температур")

        # ==================== ФИНАЛЬНЫЕ РЕКОМЕНДАЦИИ ====================
        if not recs:
            recs.append("Рекомендуется стандартная прямая укладка со стандартными материалами")

        return recs

    def show_result(self, recommendations):
        print("="*70)
        print("               РЕЗУЛЬТАТ РАБОТЫ ЭКСПЕРТНОЙ СИСТЕМЫ")
        print("="*70)

        print("РЕКОМЕНДУЕМЫЙ СПОСОБ УКЛАДКИ И МАТЕРИАЛЫ:\n")
        for i, rec in enumerate(recommendations, 1):
            print(f"  {i:2}. {rec}")

        print("\n" + "-"*70)
        print("ДОПОЛНИТЕЛЬНЫЕ РЕКОМЕНДАЦИИ:")
        print("• Всегда используйте средства индивидуальной защиты")
        print("• Соблюдайте температурный режим при работе")
        print("• После укладки дайте клею полностью высохнуть перед затиркой")
        print("="*70)


# ===================== ЗАПУСК ПРОГРАММЫ =====================
if __name__ == "__main__":
    es = TileExpertSystem()
    es.collect_input()
    recommendations = es.infer()
    es.show_result(recommendations)